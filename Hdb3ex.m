%%% MATLAB Script to illustrate HDB3 Line
% Coding scheme;
clc; clear all; close all;
hdb=[5*ones(1,100),zeros(1,100),-5*ones(1,100),...
    zeros(1,100),zeros(1,100),zeros(1,100),...
    -5*ones(1,100),zeros(1,100),5*ones(1,100),...
    -5*ones(1,100),5*ones(1,100),zeros(1,100),...
    zeros(1,100),5*ones(1,100),-5*ones(1,100),...
    5*ones(1,100),-5*ones(1,100),zeros(1,100),...
    zeros(1,100),-5*ones(1,100),...
    zeros(1,100),zeros(1,100)];
t=linspace(0,length(hdb),length(hdb));
plot(t,hdb,'Linewidth',2);title('A HDB3 Pulse Stream');
axis([0 2200 -5.5 5.5]);
xlabel('Time'); ylabel('Amplitude');
gtext('1010 0000 1100 0011 0000 00');
grid;
